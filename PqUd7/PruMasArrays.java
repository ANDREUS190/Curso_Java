package PqUd7;

public class PruMasArrays 
{
    public static void main(String[] args) 
    {
        int[] arrEnt = {1,2,3,4,5,6};
        
        for(int k=0; k<=arrEnt.length -1; k=k+1)
        {
            System.out.println(arrEnt[k]);
        }
        
        String[] arrCad = {"uno", "dos" ,"tres"};
        
        for(int k=0; k<=arrCad.length -1; k=k+1)
        {
            System.out.println(arrCad[k]);
        }
    }
}
